package ch02;

public class Ch0306Ex4pp {
	public static void main(String[] args){
		int a = 10, b = 10;
		System.out.println(" Before a : " + a + " b :" + b);
		System.out.println(" a++      : " + a++);
		System.out.println(" ++b      : " + ++b);
		System.out.println(" After a : " + a + " b :" + b);
	}
}
